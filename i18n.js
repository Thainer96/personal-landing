function setLanguage(lang) {
  const t = translations[lang];
  if (!t) return;

  document.documentElement.lang = lang;
  localStorage.setItem('lang', lang);

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) el.textContent = t[key];
  });

  // Update CV download link
  const cvBtn = document.getElementById('cvDownload');
  if (cvBtn && t.heroCvFile) {
    cvBtn.href = t.heroCvFile;
  }

  // Update lang switch button
  const switchBtn = document.querySelector('.lang-switch');
  if (switchBtn) {
    switchBtn.textContent = t.langSwitch;
    switchBtn.setAttribute('data-lang', lang === 'es' ? 'en' : 'es');
  }
}

// Init
document.addEventListener('DOMContentLoaded', () => {
  const saved = localStorage.getItem('lang') || 'es';
  setLanguage(saved);

  const switchBtn = document.querySelector('.lang-switch');
  if (switchBtn) {
    switchBtn.addEventListener('click', (e) => {
      e.preventDefault();
      const target = switchBtn.getAttribute('data-lang') || 'en';
      setLanguage(target);
    });
  }
});
